    private void loadBookTable() {
        try {
            BooksController controller = new BooksController();
            List<BooksModel> books = controller.getBooksByPageImp(currentPage, pageSize);
            int totalBooks = controller.getTotalBookCount();
            totalPages = (int) Math.ceil((double) totalBooks / pageSize);

            DefaultTableModel model = new DefaultTableModel();
            model.setColumnIdentifiers(new Object[]{"STT", "Tên sách", "Thể loại", "Số lượng"});

            int stt = (currentPage - 1) * pageSize + 1;
            for (BooksModel book : books) {
                model.addRow(new Object[]{
                    stt++,
                    book.getTitle(),
                    book.getCategory(),
                    book.getQuantity()
                });
            }

            display_bookList.setModel(model);

            // Căn giữa bảng
            DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
            centerRenderer.setHorizontalAlignment(JLabel.CENTER);
            for (int i = 0; i < display_bookList.getColumnCount(); i++) {
                display_bookList.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
            }

            // Vô hiệu hóa nút nếu ở đầu/cuối
            btn_first.setEnabled(currentPage > 1);
            btn_back.setEnabled(currentPage > 1);
            btn_next.setEnabled(currentPage < totalPages);
            btn_last.setEnabled(currentPage < totalPages);

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi tải dữ liệu sách: " + e.getMessage());
        }
    }

