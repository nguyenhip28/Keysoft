    private void btn_bookImportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_bookImportActionPerformed
        try {
            int selectedRow = display_bookList.getSelectedRow();
            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Hãy chọn sách để nhập.");
                return;
            }

            // Lấy danh sách sách của trang hiện tại
            BooksController bookController = new BooksController();
            List<BooksModel> books = bookController.getBooksByPageImp(currentPage, pageSize);

            // Kiểm tra nếu selectedRow vượt quá danh sách
            if (selectedRow >= books.size()) {
                JOptionPane.showMessageDialog(this, "Dữ liệu sách không hợp lệ.");
                return;
            }

            BooksModel selectedBook = books.get(selectedRow);
            int bookId = selectedBook.getBookId();

            // Kiểm tra số lượng nhập vào
            String quantityStr = lbl_quantity.getText().trim();
            if (quantityStr.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập số lượng sách.");
                return;
            }

            int quantity = Integer.parseInt(quantityStr);
            if (quantity <= 0) {
                JOptionPane.showMessageDialog(this, "Số lượng phải lớn hơn 0.");
                return;
            }

            // Kiểm tra sách có tồn tại trong CSDL không
            BooksModel bookInDB = bookController.getBookById(bookId);
            if (bookInDB == null) {
                JOptionPane.showMessageDialog(this, "Sách không tồn tại trong cơ sở dữ liệu.");
                return;
            }

            // Tiến hành nhập sách
            BookImportController importController = new BookImportController();
            BookImportModel importModel = new BookImportModel(new java.util.Date(), 1); // Giả sử member_id = 1
            int importId = importController.createBookImport(importModel);

            // Tạo chi tiết nhập
            BookImportDetailModel detailModel = new BookImportDetailModel(importId, bookId, quantity);
            importController.addBookImportDetail(detailModel);

            // Cập nhật số lượng sách
            importController.updateBookStock(bookId, quantity);

            JOptionPane.showMessageDialog(this, "Nhập sách thành công!");
            loadBookTable();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Số lượng nhập phải là số nguyên hợp lệ.");
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Lỗi khi nhập sách: " + e.getMessage());
        }
    }//GEN-LAST:event_btn_bookImportActionPerformed

