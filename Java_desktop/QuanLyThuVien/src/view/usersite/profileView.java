    private void loadUserInfo() {
        UserController controller = new UserController();
        UserModel user = controller.getUserByCode(userCode);

        if (user != null) {
            lbl_fullname.setText(user.getFullName());
            lbl_age.setText(String.valueOf(user.getAge()));
            lbl_email1.setText(user.getEmail());
            lbl_address.setText(user.getAddress());
            lbl_phone.setText(user.getPhone());
            jComboBox1.setSelectedItem(user.getGender());
        } else {
            JOptionPane.showMessageDialog(this, "Không tìm thấy thông tin người dùng.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }

