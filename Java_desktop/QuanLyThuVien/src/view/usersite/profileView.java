    private void btn_logoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_logoutActionPerformed
        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Bạn có chắc chắn muốn đăng xuất không?",
                "Xác nhận đăng xuất",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE
        );

        if (confirm == JOptionPane.YES_OPTION) {
            new LoginView().setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_btn_logoutActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        String fullName = lbl_fullname.getText().trim();
        String email = lbl_email1.getText().trim();
        String address = lbl_address.getText().trim();
        String phone = lbl_phone.getText().trim();
        String gender = (String) jComboBox1.getSelectedItem();
        int age;

        try {
            age = Integer.parseInt(lbl_age.getText().trim());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Tuổi phải là số nguyên.", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Tạo model mới
        UserModel updatedUser = new UserModel(
                userCode, fullName, "", email, age, gender, address, phone, "" 
        );

        // Gọi controller cập nhật
        UserController controller = new UserController();
        boolean success = controller.updateUserInfo(updatedUser);

        if (success) {
            JOptionPane.showMessageDialog(this, "Cập nhật thông tin thành công!");
        } else {
            JOptionPane.showMessageDialog(this, "Cập nhật thất bại.", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btn_updateActionPerformed

