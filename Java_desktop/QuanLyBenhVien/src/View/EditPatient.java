    private void loadPatientData() {
        if (patient != null) {
            lb_benhnhan_code.setText(patient.getPatientCode());
            lb_fullname.setText(patient.getFullName());
            lb_address.setText(patient.getAddress());
            lb_email.setText(patient.getEmail());
            lb_phonenumber.setText(patient.getPhoneNumber());

            try {
                java.util.Date date = new SimpleDateFormat("yyyy-MM-dd").parse(patient.getDateOfBirth());
                jDateChooser1.setDate(date);
            } catch (Exception e) {
                e.printStackTrace();
            }

            cb_gender.setSelectedItem(patient.getGender());
            lb_benhnhan_code.setEditable(false);
        }
    }

