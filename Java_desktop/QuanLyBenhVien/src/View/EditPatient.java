    private void btn_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_editActionPerformed
        if (!validateInput()) {
            return;
        }

        PatientModel updatedPatient = createPatientModelFromInput();

        try {
            boolean success = controller.updatePatient(updatedPatient);
            if (success) {
                JOptionPane.showMessageDialog(this, "Cập nhật thông tin bệnh nhân thành công!");
                this.dispose();
                if (parent != null) {
                    parent.setVisible(true);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Cập nhật thất bại. Vui lòng thử lại.");
            }
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, "Lỗi khi cập nhật dữ liệu: " + e.getMessage());
        }
    }

    private boolean validateInput() {
        if (lb_benhnhan_code.getText().trim().isEmpty()
                || lb_fullname.getText().trim().isEmpty()
                || jDateChooser1.getDate() == null
                || cb_gender.getSelectedItem() == null
                || lb_address.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin bắt buộc.");
            return false;
        }
        return true;
    }

    private PatientModel createPatientModelFromInput() {
        String dob = new SimpleDateFormat("yyyy-MM-dd").format(jDateChooser1.getDate());

        return new PatientModel(
                lb_benhnhan_code.getText().trim(),
                lb_fullname.getText().trim(),
                dob,
                (String) cb_gender.getSelectedItem(),
                lb_address.getText().trim(),
                lb_phonenumber.getText().trim(),
                lb_email.getText().trim()
        );
    }//GEN-LAST:event_btn_editActionPerformed

    private void btn_backActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_backActionPerformed
        this.dispose(); // Close the current add_benhnhan window
        if (parent != null) {
            parent.setVisible(true); // Show the parent window (benhnhan_manage)
        } else {
            // Fallback: Open a new benhnhan_manage window with userCode and userRole
            new ManagePatient(null, userCode, userRole).setVisible(true);
        }
    }//GEN-LAST:event_btn_backActionPerformed

