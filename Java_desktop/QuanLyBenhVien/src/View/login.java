    private void btn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_loginActionPerformed
        String username = lb_username.getText();
        String password = new String(lb_password.getPassword());

        // Sử dụng controller để xác thực
        LoginController loginController = new LoginController();
        UserModel user = loginController.authenticateUser(username, password);

        if (user != null) {
            JOptionPane.showMessageDialog(this, "Đăng nhập " + user.getRoleName() + " thành công!");
            this.setVisible(false); // Ẩn form login

            // Mở form tương ứng, truyền userCode
            switch (user.getRoleName()) {
                case "Admin" ->
                    new admin(user.getUserCode()).setVisible(true);
                case "Bác Sĩ" ->
                    new doctor(user.getUserCode()).setVisible(true);
                case "Lễ tân", "Le tan" ->
                    new letan(user.getUserCode()).setVisible(true);
                default -> {
                    JOptionPane.showMessageDialog(this, "Vai trò chưa được hỗ trợ.");
                    this.setVisible(true); // Hiển thị lại login nếu không xác định
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Sai tên đăng nhập hoặc mật khẩu.");
        }
    }//GEN-LAST:event_btn_loginActionPerformed

